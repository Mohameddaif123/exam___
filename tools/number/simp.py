## add function
def add():
    # Prompt the user to input the first number for the addition operation
    x = int(input("Enter the first number in the add operation: "))
    # Prompt the user to input the second number for the addition operation
    y = int(input("Enter the second number in the add operation: "))
    # Return the sum of the two numbers
    return x + y

## subtract function
def subtract():
    # Prompt the user to input the first number for the subtraction operation
    x = int(input("Enter the first number in the sub operation: "))
    # Prompt the user to input the second number for the subtraction operation
    y = int(input("Enter the second number in the sub operation: "))
    # Return the result of subtracting the second number from the first number
    return x - y
